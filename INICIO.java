    private void boton_modificar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_modificar1ActionPerformed
        // botón de modificar
        int n = obj.modificacion(Integer.parseInt(txt_toma_de_encargo.getText()), txt_tecnico.getText(), 
                 txt_tipo_de_pedido.getText(), txt_Proveedor.getText(), txt_estado_pedido.getText(),
                txt_producto.getText(),txt_repuesto.getText(),Integer.parseInt(txt_codigo_repuesto.getText()));
        if (n>0){
            //b.listado(txtarea);
           
            txtarea.setText(obj.listado());
            txtarea.append("Modificacion correcta \n");
        }
        else{
            txtarea.append ("NO se ha modificado \n");
        }
    }//GEN-LAST:event_boton_modificar1ActionPerformed

