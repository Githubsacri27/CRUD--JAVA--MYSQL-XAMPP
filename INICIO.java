    private void boton_insertar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_insertar1ActionPerformed
     // botón de insertar altas
      
        obj.setToma_de_encargo(Integer.parseInt(txt_toma_de_encargo.getText()));
        obj.setTecnico(txt_tecnico.getText());
        obj.setTipo_pedido(txt_tipo_de_pedido.getText());
        obj.setProveedor(txt_Proveedor.getText());
        obj.setEstado_pedido(txt_estado_pedido.getText());
        obj.setProducto(txt_producto.getText());
        obj.setRepuesto(txt_repuesto.getText());
        obj.setCodigo_repuesto(Integer.parseInt(txt_codigo_repuesto.getText()));
        
        
        
        int n= obj.insertar();
        if (n>0){
            this.txtarea.append("Se ha dado el alta" + System.lineSeparator());
        }
        else{
             this.txtarea.append("NO Se ha dado el alta" + System.lineSeparator());
        }
          
    
    }//GEN-LAST:event_boton_insertar1ActionPerformed

